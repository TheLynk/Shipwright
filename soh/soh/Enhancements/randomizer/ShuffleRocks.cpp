extern "C" void DrawItemCircle(PlayState* play, float scale, RockIdentity rockIdentity) {
    GraphicsContext* __gfxCtx = play->state.gfxCtx;
    int csmc = CVarGetInteger(CVAR_ENHANCEMENT("ChestSizeAndTextureMatchContents"), CSMC_DISABLED);
    int requiresStoneAgony = CVarGetInteger(CVAR_ENHANCEMENT("ChestSizeDependsStoneOfAgony"), 0);

    u8 r1 = 200, g1 = 200, b1 = 200, r2 = 100, g2 = 100, b2 = 100;

    if ((csmc == CSMC_BOTH || csmc == CSMC_TEXTURE) &&
        (!requiresStoneAgony || (requiresStoneAgony && CHECK_QUEST_ITEM(QUEST_STONE_OF_AGONY)))) {
        auto itemEntry = Rando::Context::GetInstance()->GetFinalGIEntry(rockIdentity.randomizerCheck, true, GI_NONE);
        GetItemCategory getItemCategory = itemEntry.getItemCategory;

        switch (getItemCategory) {
            case ITEM_CATEGORY_LESSER:
                switch (itemEntry.itemId) {
                    case ITEM_HEART_PIECE:
                    case ITEM_HEART_PIECE_2:
                    case ITEM_HEART_CONTAINER:
                        r1 = 160;
                        g1 = 80;
                        b1 = 20;
                        r2 = 40;
                        g2 = 160;
                        b2 = 160;
                        break;
                    default:
                        r1 = 160;
                        g1 = 140;
                        b1 = 80;
                        r2 = 80;
                        g2 = 70;
                        b2 = 40;
                        break;
                }
                break;
            case ITEM_CATEGORY_SMALL_KEY:
                r1 = 144;
                g1 = 144;
                b1 = 144;
                r2 = 128;
                g2 = 128;
                b2 = 128;
                break;
            case ITEM_CATEGORY_BOSS_KEY:
                r1 = 240;
                g1 = 220;
                b1 = 0;
                r2 = 128;
                g2 = 128;
                b2 = 128;
                break;
            case ITEM_CATEGORY_SKULLTULA_TOKEN:
                r1 = 24;
                g1 = 20;
                b1 = 0;
                r2 = 96;
                g2 = 80;
                b2 = 0;
                break;
            case ITEM_CATEGORY_MAJOR:
                r1 = 240;
                g1 = 220;
                b1 = 0;
                r2 = 240;
                g2 = 220;
                b2 = 0;
                break;
            default:
                break;
        }
    }

    Matrix_Scale(scale, 1.0, scale, MTXMODE_APPLY);
    gSPMatrix(POLY_XLU_DISP++, MATRIX_NEWMTX(play->state.gfxCtx), G_MTX_NOPUSH | G_MTX_LOAD | G_MTX_MODELVIEW);
    gSPSegment(POLY_XLU_DISP++, 0x08,
               (uintptr_t)Gfx_TwoTexScroll(play->state.gfxCtx, 0, 0, 0, 16, 32, 1, 0, (play->gameplayFrames * 10) % 128,
                                           16, 32));
    gDPSetPrimColor(POLY_XLU_DISP++, 0x80, 0x80, r1, g1, b1, 255);
    gDPSetEnvColor(POLY_XLU_DISP++, r2, g2, b2, 255);
    gSPDisplayList(POLY_XLU_DISP++, (Gfx*)gHoverBootsCircleDL);
}

extern "C" void EnIshi_RandomizerDraw(Actor* thisx, PlayState* play) {
    auto rockActor = ((EnIshi*)thisx);
    const auto rockIdentity = ObjectExtension::GetInstance().Get<RockIdentity>(thisx);

    OPEN_DISPS(play->state.gfxCtx);
    Gfx_SetupDL_25Opa(play->state.gfxCtx);

    if (rockActor->actor.params & 1) {
        gSPMatrix(POLY_OPA_DISP++, MATRIX_NEWMTX(play->state.gfxCtx), G_MTX_NOPUSH | G_MTX_LOAD | G_MTX_MODELVIEW);
        gDPSetPrimColor(POLY_OPA_DISP++, 0, 0, 255, 255, 255, 255);
        gSPDisplayList(POLY_OPA_DISP++, (Gfx*)gSilverRockDL);
    } else {
        Gfx_DrawDListOpa(play, (Gfx*)gFieldKakeraDL);
    }

    if (rockIdentity != nullptr && rockIdentity->randomizerCheck != RC_MAX &&
        Flags_GetRandomizerInf(rockIdentity->randomizerInf) == 0) {

        Matrix_Translate(rockActor->actor.world.pos.x,
                         rockActor->actor.world.pos.y + (rockActor->actor.params & 1 ? 32 : 8),
                         rockActor->actor.world.pos.z, MTXMODE_NEW);
        DrawItemCircle(play, (rockActor->actor.params & 1 ? 8 : 2), *rockIdentity);
    }

    CLOSE_DISPS(play->state.gfxCtx);
}

