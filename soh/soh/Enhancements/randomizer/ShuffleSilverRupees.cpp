CustomMessage GetSilverRupeeItemMessage(uint16_t rgid) {
    CustomMessage messageEntry;
    if (rgid >= RG_SILVER_RUPEE_FIRST && rgid <= RG_SILVER_RUPEE_LAST) {
        messageEntry =
            CustomMessageManager::Instance->RetrieveMessage(Randomizer::getItemMessageTableID, RG_SILVER_RUPEE_FIRST);
        auto ctx = OTRGlobals::Instance->gRandoContext;
        bool complete = ctx->GetSilverRupeeCounter(static_cast<RandomizerGet>(rgid)).AllCollected();
        if (complete) {
            messageEntry.Replace("[[count_text]]", "That's all of them");
        } else {
            int srCount = ctx->GetSilverRupeeCounter(static_cast<RandomizerGet>(rgid)).GetCollected() + 1;
            messageEntry.Replace("[[count_text]]", CustomMessage("You have collected %g[[count]]%w of them so far"));
            messageEntry.Replace("[[count]]", std::to_string(srCount));
        }
        messageEntry.Replace(
            "[[rupee_name]]",
            CustomMessage(Rando::StaticData::RetrieveItem(static_cast<RandomizerGet>(rgid)).GetName()));
        // Remove the "MQ" text from the item name to avoid *some* MQ dungeon spoiling.
        messageEntry.Replace(" MQ", CustomMessage(""));
        messageEntry.AutoFormat();
    }
    return messageEntry;
}

extern "C" void EnGSwitch_RandomizerDraw(Actor* thisx, PlayState* play) {
    EnGSwitch* srActor = reinterpret_cast<EnGSwitch*>(thisx);
    Matrix_Push();
    Matrix_Scale(17.5f, 17.5f, 17.5f, MTXMODE_APPLY);
    if (srActor->type == ENGSWITCH_SILVER_RUPEE) {
        if (CVarGetInteger(CVAR_RANDOMIZER_ENHANCEMENT("MysteriousShuffle"), 0)) {
            EnItem00_CustomItemsParticles(thisx, play, GET_ITEM_MYSTERY);
            GetItemEntry_Draw(play, GET_ITEM_MYSTERY);
        } else {
            SilverRupeeIdentity* srIdentity = ObjectExtension::GetInstance().Get<SilverRupeeIdentity>(srActor);
            EnItem00_CustomItemsParticles(thisx, play, srIdentity->itemEntry);
            GetItemEntry_Draw(play, srIdentity->itemEntry);
        }
        Matrix_Pop();
    }
}

SilverRupeeIdentity IdentifySilverRupee(Vec3f_ pos) {
    SilverRupeeIdentity srIdentity;
    uint16_t sceneNum = gPlayState->sceneNum;
    int16_t param1 = (int16_t)pos.x;
    int16_t param2 = (int16_t)pos.z;
    // Two particular silver rupees in Master Quest Shadow Temple have
    // the same X and Z coordinates. We have to make an exception here
    // and look them up by X and Y coordinates instead.
    if (sceneNum == SCENE_SHADOW_TEMPLE && param1 == 2110 && param2 == 3372) {
        param2 = (int16_t)pos.y;
    }
    Rando::Location* location = OTRGlobals::Instance->gRandomizer->GetCheckObjectFromActor(
        ACTOR_EN_G_SWITCH, sceneNum, TWO_ACTOR_PARAMS(param1, param2));
    if (location->GetRandomizerCheck() == RC_UNKNOWN_CHECK) {
        LUSLOG_WARN("SilverRupeeIdentity did not receive a valid RC value (%d).", location->GetRandomizerCheck());
        assert(false);
    } else {
        srIdentity.index.randomizerCheck = location->GetRandomizerCheck();
        srIdentity.itemEntry =
            OTRGlobals::Instance->gRandoContext->GetFinalGIEntry(srIdentity.index.randomizerCheck, true, GI_NONE);
        srIdentity.randomizerInf = static_cast<RandomizerInf>(location->GetCollectionCheck().flag);
    }
    return srIdentity;
}

